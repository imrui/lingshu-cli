test('sync 默认 auto：仅同步 baseline + 已存在产物的 personal', () => {
  freshTmp();
  runCli(['init', 'auto-sync', '--no-git', '--no-install-hooks']);
  const proj = join(TMP, 'auto-sync');

  // 第一次 lingshu sync（auto）→ 不应主动激活 personal 工具
  let r = runCli(['sync'], { cwd: proj });
  assert.equal(r.status, 0);
  for (const personal of ['.cursor/rules/lingshu-core.mdc', '.trae/rules/lingshu-core.md']) {
    assert.ok(!existsSync(join(proj, personal)), `auto 不应主动创建: ${personal}`);
  }

  // 显式 --only=cursor 激活
  r = runCli(['sync', '--only=cursor'], { cwd: proj });
  assert.equal(r.status, 0);
  assert.ok(existsSync(join(proj, '.cursor/rules/lingshu-core.mdc')), '--only=cursor 应生成 cursor 产物');

  // 再次 auto sync：cursor 已存在 → 自动维护，其余仍不激活
  r = runCli(['sync'], { cwd: proj });
  assert.equal(r.status, 0);
  assert.ok(existsSync(join(proj, '.cursor/rules/lingshu-core.mdc')), 'auto 应持续维护已激活工具');
  assert.ok(!existsSync(join(proj, '.trae/rules/lingshu-core.md')), '未激活工具仍不应被动激活');
});

