/** 在 dir 下构造一个 v0.2.x 结构的合成项目 */
function buildV02Project(dir) {
  mkdirSync(join(dir, '.lingshu/config'), { recursive: true });
  mkdirSync(join(dir, '.lingshu/scripts'), { recursive: true });
  mkdirSync(join(dir, 'reference/rules'), { recursive: true });
  mkdirSync(join(dir, '.github/workflows'), { recursive: true });

  writeFileSync(join(dir, '.lingshu/config/adapters.mjs'), `
export const sources = [
  { name: 'lingshu-core', path: 'reference/rules/lingshu-core.md' },
  { name: 'ai-behavior', path: 'reference/rules/ai-behavior.md' },
];
export const adapters = {
  cursor: { type: 'directory', target: '.cursor/rules/', extension: '.mdc', frontmatter: {
    'lingshu-core': { name: 'lingshu-core', description: '核心准则', globs: '**/*', trigger: 'always_on' },
    'ai-behavior': { name: 'ai-behavior', description: '行为准则', globs: '**/*', trigger: 'always_on' },
  } },
  'claude-code': { type: 'file', target: 'CLAUDE.md' },
  codex: { type: 'file', target: 'AGENTS.md' },
};
export const baseline = ['claude-code', 'codex'];
`, 'utf8');
  writeFileSync(join(dir, '.lingshu/scripts/sync-rules.mjs'), '// 旧引擎占位\n', 'utf8');
  // 规则文件：无 frontmatter
  writeFileSync(join(dir, 'reference/rules/lingshu-core.md'), '# 核心\n\n正文。\n', 'utf8');
  writeFileSync(join(dir, 'reference/rules/ai-behavior.md'), '# 行为\n\n正文。\n', 'utf8');
  // 脚手架 package.json
  writeFileSync(join(dir, 'package.json'), JSON.stringify({
    name: 'legacy', version: '0.0.0',
    scripts: { sync: 'node .lingshu/scripts/sync-rules.mjs' },
  }, null, 2), 'utf8');
  writeFileSync(join(dir, '.gitignore'), '.cursor/rules/\n.trae/rules/\n', 'utf8');
  writeFileSync(join(dir, '.github/workflows/rules-consistency.yml'),
    'on:\n  push:\n    paths:\n      - \'.lingshu/**\'\nsteps:\n  - run: node .lingshu/scripts/sync-rules.mjs --check --baseline\n',
    'utf8');
}

test('upgrade：v0.2.x → v0.3 机械迁移', () => {
  freshTmp();
  const proj = join(TMP, 'v02-proj');
  buildV02Project(proj);

  const r = runCli(['upgrade'], { cwd: proj });
  if (r.status !== 0) { console.error('STDOUT:', r.stdout); console.error('STDERR:', r.stderr); }
  assert.equal(r.status, 0, 'upgrade 退出码应为 0');

  // .lingshu 与 package.json 应被删除
  assert.ok(!existsSync(join(proj, '.lingshu')), '.lingshu/ 应被删除');
  assert.ok(!existsSync(join(proj, 'package.json')), '纯脚手架 package.json 应被删除');

  // 规则文件应被注入 frontmatter（含原 cursor 的 description）
  const core = readFileSync(join(proj, 'reference/rules/lingshu-core.md'), 'utf8');
  assert.match(core, /^---\r?\norder: 1/, '应注入 order: 1');
  assert.match(core, /description: 核心准则/, '应迁移原 cursor frontmatter 的 description');

  // 基线产物应被重生成
  assert.ok(existsSync(join(proj, 'CLAUDE.md')), '应重生成 CLAUDE.md');
  assert.ok(existsSync(join(proj, 'AGENTS.md')), '应重生成 AGENTS.md');

  // CI 应改为 npx 调用
  const ci = readFileSync(join(proj, '.github/workflows/rules-consistency.yml'), 'utf8');
  assert.match(ci, /npx -y @ruobai\/lingshu sync --check --baseline/, 'CI 应改造为 npx');
  assert.ok(!ci.includes('.lingshu/'), 'CI 不应再引用 .lingshu/');

  // 迁移后应能通过一致性校验
  const check = runCli(['sync', '--check', '--baseline'], { cwd: proj });
  assert.equal(check.status, 0, '迁移后应通过 sync --check --baseline');
});

