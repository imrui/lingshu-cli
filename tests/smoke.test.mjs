test('init 创建零侵入项目结构（无 .lingshu / 无 package.json）', () => {
  freshTmp();
  const r = runCli(['init', 'demo-lingshu', '--no-git', '--no-install-hooks']);
  if (r.status !== 0) {
    console.error('STDOUT:', r.stdout);
    console.error('STDERR:', r.stderr);
  }
  assert.equal(r.status, 0, '初始化退出码应为 0');

  const proj = join(TMP, 'demo-lingshu');

  // 应存在的治理资产与基线产物
  for (const p of [
    'reference/rules/lingshu-core.md',
    'reference/rules/ai-behavior.md',
    'CLAUDE.md',
    'AGENTS.md',
    '.gitignore',
    '.github/workflows/rules-consistency.yml',
  ]) {
    assert.ok(existsSync(join(proj, p)), `缺失: ${p}`);
  }

  // 零侵入核心断言：派生仓不应再包含引擎与 package.json
  for (const forbidden of ['.lingshu', 'package.json', '_gitignore']) {
    assert.ok(
      !existsSync(join(proj, forbidden)),
      `零侵入契约：派生仓不应包含 ${forbidden}`,
    );
  }

  // .gitignore 回归（v0.2.5）：不能被 npm 吞掉，且含通配 + personal 忽略
  const gitignore = readFileSync(join(proj, '.gitignore'), 'utf8');
  assert.match(gitignore, /\*-server\//, '.gitignore 应含肢体仓通配');
  assert.match(gitignore, /\*-ui\//, '.gitignore 应含肢体仓通配');
  assert.match(gitignore, /\.cursor\/rules\//, '.gitignore 应忽略 personal 工具产物');

  // 默认不应生成 personal 工具产物（v0.2.3 行为保留）
  for (const personal of [
    '.cursor/rules/lingshu-core.mdc',
    '.trae/rules/lingshu-core.md',
    '.qoder/rules/lingshu-core.md',
    '.agent/rules/lingshu-core.md',
  ]) {
    assert.ok(!existsSync(join(proj, personal)), `默认不应生成 personal 工具产物: ${personal}`);
  }

  // CLAUDE.md：不残留模板名，且头部引用零侵入的 `lingshu sync`
  const claude = readFileSync(join(proj, 'CLAUDE.md'), 'utf8');
  assert.ok(!claude.includes('lingshu-template'), 'CLAUDE.md 不应残留模板名');
  assert.match(claude, /generated by `lingshu sync`/, 'CLAUDE.md 头部应引用 lingshu sync');
  assert.ok(!claude.includes('.lingshu/scripts'), 'CLAUDE.md 不应再引用本地引擎脚本');
});

