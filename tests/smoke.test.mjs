test('tool track/untrack 直接编辑 .gitignore', () => {
  freshTmp();
  runCli(['init', 'tool-test', '--no-git', '--no-install-hooks']);
  const proj = join(TMP, 'tool-test');
  const giPath = join(proj, '.gitignore');

  // 初始：cursor 被忽略
  assert.match(readFileSync(giPath, 'utf8'), /^\.cursor\/rules\/$/m, 'fixture：cursor 初始应被忽略');

  // track cursor → 从 .gitignore 移除
  let r = runCli(['tool', 'track', 'cursor'], { cwd: proj });
  assert.equal(r.status, 0);
  assert.ok(
    !/^\.cursor\/rules\/$/m.test(readFileSync(giPath, 'utf8')),
    'track 后 .cursor/rules/ 应从 .gitignore 移除',
  );

  // untrack cursor → 重新追加
  r = runCli(['tool', 'untrack', 'cursor'], { cwd: proj });
  assert.equal(r.status, 0);
  const giFinal = readFileSync(giPath, 'utf8');
  assert.match(giFinal, /^\.cursor\/rules\/$/m, 'untrack 后应重新忽略 .cursor/rules/');
  // 幂等：不应重复追加
  const count = (giFinal.match(/^\.cursor\/rules\/$/gm) || []).length;
  assert.equal(count, 1, 'untrack 应幂等，不重复追加');

  // tool list 应可运行
  r = runCli(['tool', 'list'], { cwd: proj });
  assert.equal(r.status, 0);
  assert.match(r.stdout, /cursor/);
});

